def to_thai_numeral(num):
    thai_nums = {'0': '๐', '1': '๑', '2': '๒', '3': '๓', '4': '๔', '5': '๕', '6': '๖', '7': '๗', '8': '๘', '9': '๙'}
    return ''.join(thai_nums[digit] for digit in str(num))

